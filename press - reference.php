    <h2>Midas Share Tips Update, 1 February 2016</h2>
    <p class="news_text">MIDAS SHARE TIPS UPDATE: From motorway to medical, our tip JLIF's healthy 50% return
Joanne Hart
 
Investments that provide stable, long-term, inflation-linked returns can be hard to find these days, with stock and bond markets increasingly prone to attacks of the jitters.

John Laing Infrastructure Fund aims to fill that gap. The company owns and operates hospitals in Newcastle and Vancouver, schools in Edinburgh and inner London, court houses in Avon and Somerset and part of the M40 motorway. It even owns police stations, tax offices and street lighting. Despite the wide range of these assets, they all share certain important characteristics. They provide essential services and are run under multi-decade contracts with the Government or local authorities.
 
This means that JLIF receives a secure, annual income, as long as it manages the projects properly and delivers a good service to customers. So far, the group has done just that. Midas recommended JLIF in November 2010, when it floated on the Stock Exchange at 100p. Today the shares are 117p, but there have also been generous twice-yearly dividends, so the total return for shareholders has been more than 50 per cent.
 
