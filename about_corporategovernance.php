        <h1>Corporate governance</h1>
        <h3>The Board meets at least four times a year and, should the nature of the activity of the Company require it, additional meetings may be scheduled, sometimes at short notice. Between meetings there is regular contact with the Investment Adviser and the Administrator and the Board requires to be supplied in a timely manner with information by the Investment Adviser, the Company Secretary and other Advisers in a form and of a quality appropriate to enable it to discharge its duties.</h3>

        <p>The Company intends for all Directors to be subject to annual re-election at the Annual General Meeting of the Company. The Board intends to consider the tenure of each Director after six years. The tenure of Directors is expected to be between six and nine years to allow for phased board appointments and retirements. This process will take account of any changes to the Board’s composition arising from the need to fill a casual vacancy.</p>
        <p> The Board of JLIF has considered the principles and recommendations of the AIC Code of Corporate Governance (“AIC Code”) by reference to the AIC Corporate Governance Guide for Investment Companies (“AIC Guide”). The AIC Code, as explained by the AIC Guide, addresses all the principles set out in Section 1 of the Combined Code, as well as setting out additional principles and recommendations on issues that are of specific relevance to JLIF.</p>
        <p> Procedures have been put in place to ensure compliance with the UK Corporate Governance code which was published in September 2012 and which applies to reporting periods beginning on or after 1 October 2012.</p>
        <p> The Board considers that reporting against the principles and recommendations of the AIC Code, and by reference to the AIC Guide (which incorporates the Combined Code), will provide better information to shareholders. </p>
        <h5>Audit Committee</h5>
        <p>The Audit Committee is appointed by the Board from the non-executive Directors of the Company. The Audit Committee’s terms of reference include all matters indicated by Disclosure and Transparency Rule 7.1 and the UK Corporate Governance Code. The terms of reference are considered annually by the Audit Committee and are then referred to the Board for approval. A copy of the terms of references is available upon request from the Company Secretary.         </p>
        <p> The main role and responsibilities of the Audit Committee are:
          monitoring the integrity of the financial statements of the Group and any formal announcements relating to the Group’s financial performance and reviewing significant financial reporting judgements contained therein; </p>
        <ul>
          <li> <span>reviewing the Group’s internal financial controls and unless expressly addressed by the Board itself, the Group’s internal control and risk management systems;</span> </li>
          <li> <span>making recommendations to the Board, for a resolution to be put to the shareholders for their approval in general meeting, on the appointment of the external auditor and the approval of the remuneration and terms of engagement of the external auditor;</span> </li>
          <li> <span>reviewing and monitoring the external auditor’s independence and objectivity and the effectiveness of the audit process, taking into consideration relevant UK professional and regulatory requirements;</span> </li>
          <li> <span>reviewing the Group’s accounts;</span> </li>
          <li> <span>developing and implementing a policy on the engagement of the external auditor to supply non-audit services, taking into account relevant guidance regarding the provision of non-audit services by the external audit firm; and</span> </li>
          <li> <span>reporting to the Board on how it has discharged its responsibilities.</span> </li>
        </ul>
        <p>The Audit Committee is required to report its findings to the Board, identifying any matters on which it considers that action or improvement is needed, and make recommendations on the steps to be taken. </p>
